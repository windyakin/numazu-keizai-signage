// ラベルに「雷」を含むコード（雷表示に振り分ける）
const THUNDER_CODES = new Set<number>([108, 119, 123, 125, 140, 208, 219, 240, 250, 350, 450]);

export function weatherCategory(code: number): WeatherCategory {
  if (THUNDER_CODES.has(code)) return "thunder";
  const base = TELOP_BASE[code];
  switch (base) {
    case 100:
      return "clear";
    case 300:
      return "rain";
    case 400:
      return "snow";
    case 200:
      return "cloudy";
    default:
      // 未知コード・データ欠落時は中立な「くもり」にフォールバックする
      // （誤って全件「雨」になるのを避ける）
      return "cloudy";
  }
}
